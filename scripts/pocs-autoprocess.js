import { promises as fs } from 'node:fs';
import path from 'node:path';
import { execFile } from 'node:child_process';
import { promisify } from 'node:util';
import { pathToFileURL } from 'node:url';

const execFileAsync = promisify(execFile);
const root = process.cwd();
const dataPath = path.join(root, 'data', 'pocs.json');
const generatedPath = path.join(root, 'data', 'pocs.generated.json');

function toTitleCase(value) {
  return value
    .replace(/[-_]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .split(' ')
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

function inferTitle(item) {
  if (item.title && item.title.trim() && item.title.trim() !== 'Untitled POC') {
    return item.title.trim();
  }
  if (item.entry) {
    const parts = item.entry.split('/').filter(Boolean);
    const ignored = new Set([
      'pocs',
      'public',
      'assets',
      'dist',
      'src',
      'build',
      'demo',
      'index.html'
    ]);
    const candidate = [...parts]
      .reverse()
      .find((part) => {
        const lower = part.toLowerCase();
        if (ignored.has(lower)) return false;
        if (lower.startsWith('untitled-poc-')) return false;
        return /[a-z0-9]/i.test(part);
      });
    if (candidate) return toTitleCase(candidate);
  }
  if (item.brief) {
    const urlMatch = item.brief.match(/https?:\/\/[^\s)]+/i);
    if (urlMatch) {
      const slug = urlMatch[0].split('/').filter(Boolean).pop();
      if (slug) return toTitleCase(slug);
    }
  }
  return `POC ${item.id.slice(-6).toUpperCase()}`;
}

function inferTags(item) {
  const source = `${item.brief || ''} ${item.entry || ''}`.toLowerCase();
  const tags = [];
  if (source.includes('loader')) tags.push('Loader');
  if (source.includes('webgl') || source.includes('three')) tags.push('WebGL');
  if (source.includes('canvas')) tags.push('Canvas');
  if (source.includes('svg')) tags.push('SVG');
  if (source.includes('css')) tags.push('CSS');
  if (source.includes('hover')) tags.push('Hover');
  if (source.includes('animation') || source.includes('animate')) tags.push('Animation');
  if (tags.length === 0) tags.push('Experimental', 'Interaction');
  return Array.from(new Set(tags));
}

function inferDescription(item, title, tags) {
  const tagText = tags.slice(0, 3).join(', ').toLowerCase();
  const sourceText = item.entry ? 'uploaded ZIP demo' : 'brief-based concept';
  return `${title} is an ${sourceText} focused on ${tagText} patterns, ready for review and refinement.`;
}

function hasRunnableDemo(item) {
  const hasEntry = typeof item.entry === 'string' && item.entry.trim().length > 0;
  const hasCode = typeof item.code === 'string' && item.code.trim().length > 0;
  return hasEntry || hasCode;
}

async function syncPages() {
  await execFileAsync('node', ['scripts/sync-pages.js'], { cwd: root });
}

export async function runAutoProcess() {
  const raw = await fs.readFile(dataPath, 'utf-8');
  const parsed = JSON.parse(raw);
  const items = parsed.items || [];
  const pending = items.filter((item) => item.aiPending);

  if (pending.length === 0) {
    return { processed: 0 };
  }

  const readyToProcess = pending.filter((item) => hasRunnableDemo(item));
  if (readyToProcess.length === 0) {
    return { processed: 0 };
  }

  const generated = {
    note: 'Auto-generated by pocs:autoprocess. Review before publishing.',
    items: readyToProcess.map((item, idx) => {
      const title = inferTitle(item);
      const tags = inferTags(item);
      const description = inferDescription(item, title, tags);
      return {
        draftNumber: idx + 1,
        id: item.id,
        brief: item.brief || '',
        briefImages: item.briefImages || [],
        title,
        description,
        tags,
        code: ''
      };
    })
  };

  await fs.writeFile(generatedPath, JSON.stringify(generated, null, 2), 'utf-8');

  const byId = new Map(generated.items.map((entry) => [entry.id, entry]));
  for (const item of items) {
    const update = byId.get(item.id);
    if (!update) continue;
    item.title = update.title;
    item.description = update.description;
    item.tags = update.tags;
    item.aiPending = false;
  }

  parsed.items = items;
  await fs.writeFile(dataPath, JSON.stringify(parsed, null, 2), 'utf-8');
  await syncPages();

  return { processed: pending.length };
}

const isDirectRun = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;

if (isDirectRun) {
  runAutoProcess()
    .then((result) => {
      if (result.processed === 0) {
        console.log('No pending POCs.');
        return;
      }
      console.log(`Auto-processed ${result.processed} pending POC(s).`);
    })
    .catch((err) => {
      console.error('pocs:autoprocess failed:', err);
      process.exit(1);
    });
}
